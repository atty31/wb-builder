<?php
namespace AppBundle\Entity;

/**
 * Class Contact
 * @package AppBundle\Entity
 */
class Contact
{
    /**
     * @var $name
     */
    protected $name;
    /**
     * @var $email
     */
    protected $email;
    /**
     * @var $message
     */
    protected $message;

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * @return mixed
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param $message
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }
}